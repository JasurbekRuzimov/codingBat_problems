package register;

public class User {

   String name;
   String surname;
   String phone;
   String email;
   String password;

  public User(String name, String surname, String phone, String email, String password) {
    this.name = name;
    this.surname = surname;
    this.phone = phone;
    this.email = email;
    this.password = password;
  }
}
